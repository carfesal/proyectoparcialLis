    private void validarBtn(){
        if(indiceActual == 0){
            System.out.println("Entra izq ");
            izqBtn.setEnabled(false);
        }
        if(indiceActual == productos.size()-1){
            System.out.println("Entra der ");
            derBtn.setEnabled(false);
        }
        if(indiceActual>0 && indiceActual<productos.size()-1){
            System.out.println("Entra der ---");
            izqBtn.setEnabled(true);
            derBtn.setEnabled(true);
        }
        
    }
