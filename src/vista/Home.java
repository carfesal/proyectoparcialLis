    private void eliminarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBtnActionPerformed
        // TODO add your handling code here:
        
        int indice = lista.getSelectedIndex();
        if(indice >=0){
            ItemCarrito i = dm.get(indice);
            total -= i.getCantidad()*i.getProducto().getPrecio();
            precioTotal.setText("$"+total);
            dm.remove(indice);
            JOptionPane.showMessageDialog(this, "Producto eliminado del carrito", "Exito", JOptionPane.WARNING_MESSAGE);
            
        }else{
           JOptionPane.showMessageDialog(this, "Elija un item del carrito para poder eliminar", "Error", JOptionPane.ERROR_MESSAGE); 
        }
        
        
    }//GEN-LAST:event_eliminarBtnActionPerformed

    private void agregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBtnActionPerformed
        // TODO add your handling code here:
        int cantidad = (Integer)cantidadSpinner.getValue();
        if(cantidad <= 0){
            JOptionPane.showMessageDialog(this, "Elija una cantidad mayor a 0", "Cantidad Erronea", JOptionPane.ERROR_MESSAGE);
        }else{
            ItemCarrito i = new ItemCarrito(productos.get(indiceActual),cantidad);
            dm.addElement(i);
            lista.removeAll();
            lista.setModel(dm);
            total += productos.get(indiceActual).getPrecio()*(Integer)cantidadSpinner.getValue();
            precioTotal.setText("$"+total);
            cantidadSpinner.setValue(0);
            JOptionPane.showMessageDialog(this, "Producto agregado al carrito", "Exito", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_agregarBtnActionPerformed

